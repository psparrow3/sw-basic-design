	// 백그라운드(맵) 버퍼 생성
	std::vector<char> buffer(SCREEN_WIDTH* SCREEN_HEIGHT, ' ');

	// 맵 그리기 - 맵은 고정되어 있으므로 초기화 후 다시 그리지 않음
	
	a.drawBitmap("bottom.bmp", buffer, 0, 480, SCREEN_WIDTH);
	
	a.flushBuffer(buffer, SCREEN_WIDTH, SCREEN_HEIGHT);

	int flag = 0;
	int x = 18;
	int time = 0, time_flag = 0;

	drawCharacter ac;

	ac.characterRightDraw(40, 420, buffer);
	
	while (1)
	{
		a.drawBitmap("Empty_boss.bmp", buffer, b.m_x, b.m_y, SCREEN_WIDTH);
	
		if (time >= 1000)
		{
			b.Boss1Attack();
			time = 0;
			time_flag = 1;
		}

		if (!flag)
		{
			x--;
			b.Boss1Move(x);

			if (x == 0)
			{
				flag = 1;
			}
		}
		else
		{
			x++;
			b.Boss1Move(x);
	
			if (x == 18)
			{
				flag = 0;
			}
		}

		a.drawBitmap("Boss2.bmp", buffer, b.m_x, b.m_y, SCREEN_WIDTH);

		a.flushBuffer(buffer, SCREEN_WIDTH, SCREEN_HEIGHT);

		b.Boss1AttackMove(buffer);
		
		Sleep(100);

		time += 100;
	}
	
	getchar();


	return 0;
